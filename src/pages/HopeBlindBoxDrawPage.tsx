export const HopeBlindBoxDrawPage = () => {
  const navigate = useNavigate();

  useEffect(() => {
    // 记录今天已经抽取过盲盒
    const today = new Date().toISOString().split('T')[0];
    localStorage.setItem('last_blind_box_draw_date', today);

    // 摇晃动画3秒后抽取盲盒
    const timer = setTimeout(() => {
      drawBlindBox();
    }, 3000);

    return () => clearTimeout(timer);
  }, []);

  const drawBlindBox = () => {
    // 获取用户的许愿类型
    const wishType = localStorage.getItem('blind_box_wish_type') || '微光学业';

    // 随机选择一个盲盒类型
    const allTypes = Object.keys(blindBoxTasks) as BlindBoxType[];
    const randomType = allTypes[Math.floor(Math.random() * allTypes.length)];

    // 从选中类型中随机选择一个任务
    const tasks = blindBoxTasks[randomType];
    const randomTask = tasks[Math.floor(Math.random() * tasks.length)];

    // 保存盲盒记录
    const blindBoxRecord = {
      id: Date.now().toString(),
      date: new Date().toISOString().split('T')[0],
      wishType,
      actualType: randomType,
      task: randomTask,
      status: 'pending' as const,
    };

    saveHopeBlindBox(blindBoxRecord);

    // 保存到localStorage以便后续页面使用
    localStorage.setItem('current_blind_box', JSON.stringify(blindBoxRecord));

    // 根据是否抽中许愿类型跳转到不同页面
    if (randomType === wishType) {
      navigate('/mental-save/hope-blind-box/draw/success');
    } else {
      navigate('/mental-save/hope-blind-box/draw/fail');
    }
  };

  return (
    <div className="min-h-screen bg-white flex flex-col items-center justify-center">
      <div className="text-center">
        <div 
          className="w-64 h-64 animate-bounce"
          style={{ animationDuration: '0.5s', animationIterationCount: '6' }}
        >
          <img src="./assets/manghe.png" alt="盲盒" className="w-full h-full object-contain" />
        </div>
        <p className="mt-6 text-xl font-medium text-gray-700">盲盒抽取中···</p>
      </div>
    </div>
  );
};